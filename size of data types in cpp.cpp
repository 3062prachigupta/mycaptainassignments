// to identify the size of int float char double
#include<iostream>
using namespace std;
int main()
{
	cout<<"size of char:    "<<sizeof(char)<<" bytes\n"
		<<"size of int:     "<<sizeof(int)<<" bytes\n"
		<<"size of float:   "<<sizeof(float)<<" bytes\n"
		<<"size of double:  "<<sizeof(double)<<" bytes\n";
		return 0;
}
